def test_trial_runs_the_real_pipeline_and_scores_it():
    r = run_trial("PMP-01", "cavitation", seed=11)
    assert r.detected and not r.error
    assert r.case_id is not None
    assert r.detected_metric == "vibration_mm_s"
    assert r.llm_mode == "mock"
    assert r.cited_work_orders, "a scored trial must have evidence to score"
    # The mock policy reports the top history match; for a pump vibration ramp
    # with cavitation precedent WO-1007 present, it should land on cavitation.
    assert r.predicted_text == "cavitation"
    assert r.correct_text and r.correct_citation
    assert r.classifier_pred is not None
    assert isinstance(r.classifier_correct, bool)
    assert not r.classifier_abstained


