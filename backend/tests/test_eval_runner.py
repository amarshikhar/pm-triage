"""Ground-truth capture, label containment, and the trial loop.

The containment tests are the important ones. If the ground-truth label ever
reaches the agent, every accuracy number this harness prints becomes a lie, and
it would fail silently and look like a great result.
"""

import json

import pytest

from app.agent import tools as T
from app.detector import run_detection
from app.eval.metrics import summarize
from app.eval.runner import (
    FAULT_MACHINE_TYPES, build_plan, eligible_machines, replay_episodes, run_trial,
)
from app.models import Anomaly, Machine, TelemetryReading, utcnow
from app.simulator import FAULTS, FleetSimulator


def _reading(db, machine_id, **overrides):
    vals = {"temperature_c": 55, "vibration_mm_s": 2.5, "pressure_kpa": 300, "rpm": 8000}
    vals.update(overrides)
    r = TelemetryReading(machine_id=machine_id, ts=utcnow().isoformat(),
                         values_json=json.dumps(vals))
    db.add(r)
    db.commit()
    return r


# --- ground truth capture ---------------------------------------------------

def test_anomaly_records_the_active_fault(db):
    m = db.get(Machine, "CNC-01")
    r = _reading(db, m.id, vibration_mm_s=7.2)
    created = run_detection(db, m, r, ground_truth_fault="bearing_wear")
    assert db.get(Anomaly, created[0]).ground_truth_fault == "bearing_wear"


def test_organic_anomaly_has_no_label(db):
    m = db.get(Machine, "CNC-01")
    r = _reading(db, m.id, vibration_mm_s=7.2)
    created = run_detection(db, m, r)
    assert db.get(Anomaly, created[0]).ground_truth_fault is None


def test_label_does_not_change_detection(db):
    """Detection must stay a pure function of telemetry. If the label could
    influence it, detection rate would be self-fulfilling."""
    m = db.get(Machine, "CNC-01")
    normal = _reading(db, m.id)
    assert run_detection(db, m, normal, ground_truth_fault="bearing_wear") == []


def test_simulator_propagates_its_injected_fault(db):
    sim = FleetSimulator(seed=1, spontaneous_fault_prob=0.0)
    sim.inject_fault("CMP-01", "overheat")
    found = None
    for _ in range(60):
        for aid in sim.tick():
            a = db.get(Anomaly, aid)
            if a.machine_id == "CMP-01":
                found = a
                break
        if found:
            break
    assert found is not None, "overheat never breached the compressor limit"
    assert found.ground_truth_fault == "overheat"


# --- the label must never reach the agent -----------------------------------

LABEL_FIELDS = ("ground_truth_fault", "ground_truth", "fault_class", "label")


def _keys(obj) -> set:
    if isinstance(obj, dict):
        return set(obj) | {k for v in obj.values() for k in _keys(v)}
    if isinstance(obj, list):
        return {k for item in obj for k in _keys(item)}
    return set()


def test_no_agent_tool_exposes_a_label_field(db):
    """The guarantee is about fields, not vocabulary.

    WO-1007's failure_mode is the word "cavitation" — that is the CMMS doing its
    job, and finding it is exactly the reasoning we want to measure. What must
    never appear is a field carrying *this* anomaly's answer.
    """
    _reading(db, "PMP-01")
    outputs = [
        T.search_maintenance_history(db, "pump", "vibration temperature rising"),
        T.get_machine_info(db, "PMP-01"),
        T.get_recent_telemetry(db, "PMP-01"),
    ]
    assert outputs[0]["matches"], "search must return evidence for this test to mean anything"
    for out in outputs:
        leaked = _keys(out) & set(LABEL_FIELDS)
        assert not leaked, f"tool output exposes label field(s): {leaked}"


def test_cmms_records_carry_no_label_column(db):
    """The label lives in app/eval/taxonomy.py, never on the model the agent
    can read. If someone adds it to MaintenanceLog for convenience, the tool
    would serialise it straight into the prompt."""
    from app.models import MaintenanceLog
    columns = set(MaintenanceLog.__table__.columns.keys())
    assert not (columns & set(LABEL_FIELDS)), f"labels leaked onto the model: {columns}"


def test_agent_tool_surface_is_locked_down():
    """A tripwire: the label is only safe because we audited what each tool
    returns. A new tool must be checked against LABEL_FIELDS deliberately
    rather than inheriting the assumption, so adding one fails here first."""
    registered = {t["function"]["name"] for t in T.TOOL_SCHEMAS}
    assert registered == {"get_machine_info", "get_recent_telemetry",
                          "search_maintenance_history"}


def test_anomaly_label_is_not_in_the_agent_prompt(db):
    """run_triage builds its prompt from anomaly.description; that string is
    generated by the detector and must not carry the label."""
    m = db.get(Machine, "CNC-01")
    for _ in range(6):
        _reading(db, m.id)
    r = _reading(db, m.id, vibration_mm_s=7.4)
    created = run_detection(db, m, r, ground_truth_fault="bearing_wear")
    assert "bearing_wear" not in db.get(Anomaly, created[0]).description


# --- trial plan -------------------------------------------------------------

def test_faults_only_target_machines_that_can_show_them(db):
    """cnc_mill and conveyor have no pressure threshold, so a pressure_loss
    there would drift for ever and never be detected."""
    for fault, types in FAULT_MACHINE_TYPES.items():
        machines = eligible_machines(db, fault)
        assert machines, f"{fault} has no eligible machine"
        assert {m.type for m in machines} <= set(types)
    assert set(FAULT_MACHINE_TYPES) == set(FAULTS), "every simulated fault needs a policy"


def test_plan_is_balanced_and_deterministic():
    plan = build_plan(12, seed=3)
    assert len(plan) == 12
    counts = {}
    for _mid, fault, _s in plan:
        counts[fault] = counts.get(fault, 0) + 1
    assert set(counts) == set(FAULTS)
    assert max(counts.values()) - min(counts.values()) <= 1, "fault classes unbalanced"
    assert build_plan(12, seed=3) == plan, "same seed must give the same plan"


def test_plan_respects_eligibility():
    for machine_id, fault, _ in build_plan(16, seed=5):
        prefix = machine_id.split("-")[0]
        expected = {"CNC": "cnc_mill", "CMP": "compressor",
                    "PMP": "pump", "CNV": "conveyor"}[prefix]
        assert expected in FAULT_MACHINE_TYPES[fault]


def test_replay_plan_includes_every_physical_episode():
    episodes = replay_episodes()
    assert len(episodes) == 8
    assert len({name for _machine, _fault, name in episodes}) == 8
    assert sum(fault == "discharge_restriction"
               for _machine, fault, _name in episodes) == 2
    assert sum(machine == "BRG-01" for machine, _fault, _name in episodes) == 3


