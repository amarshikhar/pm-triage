def test_case_preserves_the_agents_own_citation_order(db):
    """The citation scorer's whole premise is that the agent leads with its
    primary analogue. evidence.historical_matches is sorted by CMMS match_score,
    so scoring it measured retrieval, not the agent. The agent's list must
    survive verbatim, order included."""
    from app.agent.triage import run_triage
    m = db.get(Machine, "PMP-01")
    for _ in range(6):
        _reading(db, m.id, pressure_kpa=400)
    r = _reading(db, m.id, vibration_mm_s=7.5, pressure_kpa=400)
    anomaly_id = run_detection(db, m, r, ground_truth_fault="cavitation")[0]

    case = run_triage(db, anomaly_id)
    evidence = case.as_dict(full=True)["evidence"]
    # A classification conflict can invalidate citations operationally, but the
    # draft ordering must remain auditable even when the guard clears it.
    agent_list = evidence["agent_draft_cited_work_orders"]
    assert agent_list, "the agent cited nothing; scorer would have no input"

    trace_cites = [t for t in case.as_dict(full=True)["trace"]
                   if t.get("tool") == "search_maintenance_history"]
    assert trace_cites, "expected the agent to search history"
    # Order is the agent's own, not a set or a re-sort of the search results.
    assert agent_list == list(dict.fromkeys(agent_list)), "citation order mangled by a set"


def test_trial_is_isolated_from_previous_trials():
    """Recurrence counting and the detector cooldown both read prior rows, so a
    repeated trial must produce an identical result."""
    a = run_trial("CMP-01", "overheat", seed=4)
    b = run_trial("CMP-01", "overheat", seed=4)
    assert a.ticks_to_detect == b.ticks_to_detect
    assert a.root_cause == b.root_cause
    assert a.case_id == b.case_id == 1, "case ids must restart, proving a fresh db"


